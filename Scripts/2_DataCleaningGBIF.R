# Checking species' coordinates -------------------------------------------


occs.coord <- PperuvianaSpecies[!is.na(PperuvianaSpecies$decimalLatitude) 
                   & !is.na(PperuvianaSpecies$decimalLongitude),]
# output w/ only potential correct coordinates
geo.clean <- clean_coordinates(x = occs.coord, 
                               lon = "decimalLongitude",
                               lat = "decimalLatitude",
                               species = "species", 
                               value = "clean")

#plot the output of clean data

plot(decimalLatitude ~ decimalLongitude, data = geo.clean)
#map(, , , add = TRUE)
par(mfrow = c(1, 1))

geo.clean_vetor <- geo.clean %>% 
  dplyr::relocate(decimalLongitude, decimalLatitude, .after = 1) %>% 
  dplyr::mutate(lon = decimalLongitude, lat = decimalLatitude) %>%
  sf::st_as_sf(coords = c("lon", "lat"), crs = 4326)

tm_shape(world, 
         bbox = geo.clean_vetor) +
  tm_polygons() +
  tm_shape(geo.clean_vetor) +
  tm_dots(size = .1, col = "forestgreen")

#setting value = clean it returns only the potentially correct coordinates. For checking and reproducibility we want to save all the output with the flags generated by the routine
occs.new.geo <- clean_coordinates(x = occs.coord, 
                                  lon = "decimalLongitude",
                                  lat = "decimalLatitude",
                                  species = "species", 
                                  value = "spatialvalid")


occs.new.geo2 <- merge(PperuvianaSpecies, occs.new.geo, 
                       all.x = TRUE, 
                       by = "gbifID")


# Remove impossible, incomplete and unlikely coordinates# -----------------

#colnames(occs.new.geo2)
occ.new.geo4 <- occs.new.geo2 %>%
  dplyr::filter(between(year.x, 1902, 2023) & countryCode.x %in% c("AR", "BR", "PY"))

write.csv(occ.new.geo4, "Pperuviana_coord_checkTOTAL.csv", 
          row.names = FALSE)

