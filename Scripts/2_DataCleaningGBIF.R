# Importing and preparing data --------------------------------------------
PperuvianaSpecies <- read_delim("GBIFPperuviana_ago23.csv", 
                                delim = "\t", escape_double = FALSE, col_types = cols(decimalLatitude = col_number(), 
                                                                                     decimalLongitude = col_number()), 
                                trim_ws = TRUE)

Pperuviana <- PperuvianaSpecies %>%
  dplyr::select(gbifID, decimalLatitude, decimalLongitude) 

#conversão para classe sf
PperuvianaSpecies_vetor <- Pperuviana %>% 
  dplyr::relocate(decimalLongitude, decimalLatitude, .after = 1) %>% 
  dplyr::mutate(lon = decimalLongitude, lat = decimalLatitude) %>%
  sf::st_as_sf(coords = c("lon", "lat"), crs = 4326)

data(world)
#plot(world[1], col = "gray", main = "Mapa do mundo")


# Plotting ocurrences (world) ---------------------------------------------
tmap::tmap_mode(mode = "plot")

tm_shape(world, 
         bbox = PperuvianaSpecies_vetor) +
  tm_polygons() +
  tm_shape(PperuvianaSpecies_vetor) +
  tm_dots(size = .1, col = "forestgreen")

# Checking species taxonomy -----------------------------------------------

sort(unique(PperuvianaSpecies$scientificName))


#Unique species list
species.names <- unique(PperuvianaSpecies$scientificName) 
tax.check <- TPL(species.names)
tax.check

# creating new object w/ original and new names after TPL
new.tax <- data.frame(scientificName = species.names, 
                      genus.new.TPL = tax.check$New.Genus, 
                      species.new.TPL = tax.check$New.Species,
                      status.TPL = tax.check$Taxonomic.status,
                      scientificName.new.TPL = paste(tax.check$New.Genus,
                                                     tax.check$New.Species)) 

#Merging raw data and checked data
occs.new.tax <- merge(PperuvianaSpecies, new.tax, by = "scientificName")

